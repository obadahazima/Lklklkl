export type TranslationKey = keyof typeof translations.ar;

export function tr(lang: Lang, key: TranslationKey, vars?: Record<string, string>): string {
  let str = (translations[lang][key] ?? translations.ar[key]) as string;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.replace(`{${k}}`, v);
    }
  }
  return str;
}

export function useTr(lang: Lang) {
  return (key: TranslationKey, vars?: Record<string, string>) => tr(lang, key, vars);
}
